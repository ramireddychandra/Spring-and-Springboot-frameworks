package com.javatest.Springboot.game;

public class MarioGame implements Gameconsole{

	public void up()
	{
		System.out.println("Its an up array to jump mario");
	}
	public void down()
	{
		System.out.println("Its an up array to down mario");
	}
	public void left()
	{
		System.out.println("Its an up array to left mario");
	}
	public void right()
	{
		System.out.println("Its an up array to right mario");
	}
}
