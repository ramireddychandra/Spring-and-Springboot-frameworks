package pratice;

public class IfElseLoop {

	public static void main(String[] args) {
		int i =1;
		
		if(i>10)
		{
			System.out.println("Value is greater than 10");
		}
		else if(i>=1)
		{
			System.out.println("Value is between 1 and 10");
		}
		else
		{
			System.out.println("Value is 0");
		}
	}

}
